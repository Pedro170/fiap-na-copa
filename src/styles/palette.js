const colors = type => {
  if( type === 'first' ) {
    return '#4F0222'
  }

  if( type === 'second' ) {
    return '#D9A511'
  }

  if( type === 'third' ) {
    return '#FFFBFE'
  }

  if( type === 'fourth' ) {
    return '#ddd'
  }
  
  if( type === 'fifth' ) {
    return '#333'
  }
}

export default colors;